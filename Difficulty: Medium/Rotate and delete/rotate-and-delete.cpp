class Solution {
  public:
    int rotateDelete(vector<int> &arr) {
        // Your code here
        int n = arr.size();
        return n == 1 ? arr[0] : (n % 2 == 0 ? arr[2 + (n - 2) / 4 - 1] : arr[3 + (n - 3) / 4 - 1]);
    }
};


