class Solution {
  public:
    int findKRotation(vector<int> &arr) {
        // Code Here
        int n = arr.size();
        int l = 0, r = n-1;
        
        while(l<=r){
            int mid = l + (r-l)/2;
            if(l == r){
                return l;
            }
            if(arr[mid]<arr[r]){
                r = mid;
            }
            else {
                l+=1;
            }
        }
        return 0;
    }
};


